package cn.onedirection.pojo;

import java.util.Date;

/**
 * 比赛/活动申请表
 */
public class ActApply { 
	private int acap_ID;           // 活动申请表id
	private int user_ID;           // 团队id
	private int act_ID;            // 活动id
	private String acap_remark;    // 申请活动的备注
	private int acap_status;	   // 1.活动 2.竞赛
	private Date acap_createtime;  // 创建时间
	public int getAcap_ID() {
		return acap_ID;
	}
	public void setAcap_ID(int acap_ID) {
		this.acap_ID = acap_ID;
	}
	public int getUser_ID() {
		return user_ID;
	}
	public void setUser_ID(int user_ID) {
		this.user_ID = user_ID;
	}
	public int getAct_ID() {
		return act_ID;
	}
	public void setAct_ID(int act_ID) {
		this.act_ID = act_ID;
	}
	public String getAcap_remark() {
		return acap_remark;
	}
	public void setAcap_remark(String acap_remark) {
		this.acap_remark = acap_remark;
	}
	public int getAcap_status() {
		return acap_status;
	}
	public void setAcap_status(int acap_status) {
		this.acap_status = acap_status;
	}
	public Date getAcap_createtime() {
		return acap_createtime;
	}
	public void setAcap_createtime(Date acap_createtime) {
		this.acap_createtime = acap_createtime;
	}
	@Override
	public String toString() {
		return "ActApply [acap_ID=" + acap_ID + ", user_ID=" + user_ID + ", act_ID=" + act_ID + ", acap_remark="
				+ acap_remark + ", acap_status=" + acap_status + ", acap_createtime=" + acap_createtime + "]";
	}

	
	
}
