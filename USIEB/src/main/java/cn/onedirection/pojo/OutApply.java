package cn.onedirection.pojo;

import java.util.Date;

/**
 * 外带/会议室申请表
 */
public class OutApply {
	private int outap_ID;          // 外带申请表id
	private int user_ID;           // 团队id
	private String outap_name;     // 外带物品名称			会议室名称
	private String outap_num;      // 外带物品数量				
	private Date outap_outdate;    // 外带开始时间			会议室起始时间
	private Date outap_predate;    // 预计归还时间			会议室截止时间
	private int outap_status;      // 借还状态			1.正在申请 2.使用中 3.使用结束 4.拒绝
	private String outap_remark;   // 备注
	private int outap_media;	   // 是否使用多媒体
	private Date outap_createtime; // 创建时间（申请时间）
	public int getOutap_ID() {
		return outap_ID;
	}
	public void setOutap_ID(int outap_ID) {
		this.outap_ID = outap_ID;
	}
	public int getUser_ID() {
		return user_ID;
	}
	public void setUser_ID(int user_ID) {
		this.user_ID = user_ID;
	}
	public String getOutap_name() {
		return outap_name;
	}
	public void setOutap_name(String outap_name) {
		this.outap_name = outap_name;
	}
	public String getOutap_num() {
		return outap_num;
	}
	public void setOutap_num(String outap_num) {
		this.outap_num = outap_num;
	}
	public Date getOutap_outdate() {
		return outap_outdate;
	}
	public void setOutap_outdate(Date outap_outdate) {
		this.outap_outdate = outap_outdate;
	}
	public Date getOutap_predate() {
		return outap_predate;
	}
	public void setOutap_predate(Date outap_predate) {
		this.outap_predate = outap_predate;
	}
	public int getOutap_status() {
		return outap_status;
	}
	public void setOutap_status(int outap_status) {
		this.outap_status = outap_status;
	}
	public String getOutap_remark() {
		return outap_remark;
	}
	public void setOutap_remark(String outap_remark) {
		this.outap_remark = outap_remark;
	}
	public int getOutap_media() {
		return outap_media;
	}
	public void setOutap_media(int outap_media) {
		this.outap_media = outap_media;
	}
	public Date getOutap_createtime() {
		return outap_createtime;
	}
	public void setOutap_createtime(Date outap_createtime) {
		this.outap_createtime = outap_createtime;
	}
	@Override
	public String toString() {
		return "OutApply [outap_ID=" + outap_ID + ", user_ID=" + user_ID + ", outap_name=" + outap_name + ", outap_num="
				+ outap_num + ", outap_outdate=" + outap_outdate + ", outap_predate=" + outap_predate
				+ ", outap_status=" + outap_status + ", outap_remark=" + outap_remark + ", outap_media=" + outap_media
				+ ", outap_createtime=" + outap_createtime + "]";
	}

}
