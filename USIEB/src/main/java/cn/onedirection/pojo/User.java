package cn.onedirection.pojo;

import java.util.Date;

public class User {

	private int user_ID;					//自动增长
	private String user_account;			//登录账号，session域
	private String user_pswd;				//登录密码
	private String user_tname;				//团队名称
	private String user_chname;				//用户姓名
	private String user_chphone;			//用户电话
	private String user_chmail;				//用户邮箱
	private int user_identyfy;				//用户身份	1.教师 2.助理 3.学生
	private int user_tstatus;				//用户状态	1.正常 2.冻结
	private int user_cardnum;				//工牌数量
	private int user_cumpnum;				//电脑数量
	private Date user_createtime;
	public int getUser_ID() {
		return user_ID;
	}
	public void setUser_ID(int user_ID) {
		this.user_ID = user_ID;
	}
	public String getUser_account() {
		return user_account;
	}
	public void setUser_account(String user_account) {
		this.user_account = user_account;
	}
	public String getUser_pswd() {
		return user_pswd;
	}
	public void setUser_pswd(String user_pswd) {
		this.user_pswd = user_pswd;
	}
	public String getUser_tname() {
		return user_tname;
	}
	public void setUser_tname(String user_tname) {
		this.user_tname = user_tname;
	}
	public String getUser_chname() {
		return user_chname;
	}
	public void setUser_chname(String user_chname) {
		this.user_chname = user_chname;
	}
	public String getUser_chphone() {
		return user_chphone;
	}
	public void setUser_chphone(String user_chphone) {
		this.user_chphone = user_chphone;
	}
	public String getUser_chmail() {
		return user_chmail;
	}
	public void setUser_chmail(String user_chmail) {
		this.user_chmail = user_chmail;
	}
	public int getUser_identyfy() {
		return user_identyfy;
	}
	public void setUser_identyfy(int user_identyfy) {
		this.user_identyfy = user_identyfy;
	}
	public int getUser_tstatus() {
		return user_tstatus;
	}
	public void setUser_tstatus(int user_tstatus) {
		this.user_tstatus = user_tstatus;
	}
	public int getUser_cardnum() {
		return user_cardnum;
	}
	public void setUser_cardnum(int user_cardnum) {
		this.user_cardnum = user_cardnum;
	}
	public int getUser_cumpnum() {
		return user_cumpnum;
	}
	public void setUser_cumpnum(int user_cumpnum) {
		this.user_cumpnum = user_cumpnum;
	}
	public Date getUser_createtime() {
		return user_createtime;
	}
	public void setUser_createtime(Date user_createtime) {
		this.user_createtime = user_createtime;
	}
	@Override
	public String toString() {
		return "User [user_ID=" + user_ID + ", user_account=" + user_account + ", user_pswd=" + user_pswd
				+ ", user_tname=" + user_tname + ", user_chname=" + user_chname + ", user_chphone=" + user_chphone
				+ ", user_chmail=" + user_chmail + ", user_identyfy=" + user_identyfy + ", user_tstatus=" + user_tstatus
				+ ", user_cardnum=" + user_cardnum + ", user_cumpnum=" + user_cumpnum + ", user_createtime="
				+ user_createtime + "]";
	}			
}
